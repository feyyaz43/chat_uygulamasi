    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (jRadioButton1.isSelected() == true) {
            out.println(client_isim + " : " + jTextField1.getText());
            jTextArea1.setText(jTextArea1.getText() + (client_isim + " : " + jTextField1.getText() + "\n"));
            jTextField1.setText("");
        } else if (jRadioButton2.isSelected() == true) {
            msg = client_isim + " : " + jTextField1.getText();
            jTextArea1.setText(jTextArea1.getText() + (client_isim + " : " + jTextField1.getText() + "\n"));
            outBuf = msg.getBytes();
            outPacket = new DatagramPacket(outBuf, 0, outBuf.length,
                    address, port);
            try {
                datagramSocket.send(outPacket);
            } catch (IOException ex) {
                Logger.getLogger(client_form.class.getName()).log(Level.SEVERE, null, ex);
            }
            jTextField1.setText("");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if (!jTextField2.getText().isEmpty() || !jTextField3.getText().isEmpty()) {
            client_isim = jTextField2.getText();
            ip = jTextField3.getText();
            port = Integer.parseInt(jTextField4.getText());
            jPanel1.setVisible(true);
            jPanel2.setVisible(false);

            if (jRadioButton1.isSelected() == true) {
                try {
                    //* server 'a localhost ve 7755 portu √ºzerinden ba≈ülantƒ± saƒülanƒ±yor *//
                    socket = new Socket(ip, port);
                } catch (Exception e) {
                    System.out.println("Port Hatasƒ±!");
                }
                try {
                    //* Server'a veri g√∂nderimi i√ßin kullandƒ±ƒüƒ±mƒ±z PrintWriter nesnesi olu≈üturduk *//
                    out = new PrintWriter(socket.getOutputStream(), true);

                    //* Server'dan gelen verileri tutan BufferedReader nesnesi olu≈üturulur *//
                    in = new BufferedReader(new InputStreamReader(socket.getInputStream()));
                } catch (IOException ex) {
                    Logger.getLogger(client_form.class.getName()).log(Level.SEVERE, null, ex);
                }

                BufferedReader data = new BufferedReader(new InputStreamReader(System.in));

                Thread yenile = new Thread(new Runnable() {

                    @Override
                    public void run() {
                        try {
                            while ((deger = in.readLine()) != null) {
                                jTextArea1.setText(jTextArea1.getText() + deger + "\n");
                            }
                        } catch (IOException ex) {
                            Logger.getLogger(client_form.class.getName()).log(Level.SEVERE, null, ex);
                        }
                    }
                });

                yenile.start();
            } else if (jRadioButton2.isSelected() == true) {
                try {
                    address = InetAddress.getByName(ip);
                } catch (UnknownHostException ex) {
                    Logger.getLogger(client_form.class.getName()).log(Level.SEVERE, null, ex);
                }
                try {
                    datagramSocket = new DatagramSocket();
                } catch (SocketException ex) {
                    Logger.getLogger(client_form.class.getName()).log(Level.SEVERE, null, ex);
                }

                inBuf = new byte[256];
                inPacket = new DatagramPacket(inBuf, inBuf.length);

                Thread yenile2 = new Thread(new Runnable() {

                    @Override
                    public void run() {
                        try {
                            while (true) {
                                datagramSocket.receive(inPacket);
                                String data = new String(inPacket.getData(), 0, inPacket.getLength());
                                jTextArea1.setText(jTextArea1.getText() + data + "\n");

                            }
                        } catch (IOException ex) {
                            Logger.getLogger(client_form.class.getName()).log(Level.SEVERE, null, ex);
                        }
                    }
                });

                yenile2.start();
            }
        } else {
            JOptionPane.showMessageDialog(rootPane, "bo≈ü bƒ±rakmayƒ±nƒ±z", "uyarƒ±", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

